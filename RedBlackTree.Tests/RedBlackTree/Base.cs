        public RedBlackTree<int> GenerateTree(int[] items)
        {
            var tree = new RedBlackTree<int>();
            foreach (var item in items)
            {
                tree.Insert(item);
            }

            return tree;
        }

        public RedBlackTree<int> GenerateRandomTree()
        {
            var tree = new RedBlackTree<int>();

            var random = new Random();
            var totalItemsInsert = random.Next(1, 1000);
            var totalItemsDelete = random.Next(1, 500);

            var items = new int[totalItemsInsert];

            RandomTreeInsertOperations(tree, items, totalItemsInsert);

            RandomTreeDeleteOperations(tree, items, totalItemsInsert, totalItemsDelete);

            return tree;
        }

        private void RandomTreeInsertOperations(RedBlackTree<int> tree, int[] items, int totalItemsInsert)
        {
            var random = new Random();

            for (int i = 0; i < totalItemsInsert; i++)
            {
                int item;

                do
                {
                    item = random.Next(1, 5000);
                } while (items.Contains(item));

                tree.Insert(item);

                items[i] = item;
            }
        }

        private void RandomTreeDeleteOperations(RedBlackTree<int> tree, int[] items, int totalItemsInsert, int totalItemsDelete)
        {
            var random = new Random();

            for (int i = 0; i < totalItemsDelete; i++)
            {
                var index = random.Next(totalItemsInsert - 1);
                var item = items[index];

                var node = tree.Search(item);

                if (node != null)
                {
                    tree.Delete(node);
                    items[index] = 0;
                }
            }
        }
